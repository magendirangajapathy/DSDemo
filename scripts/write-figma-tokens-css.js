const fs = require('fs')
const path = require('path')

const mapPath = path.join(__dirname, '..', 'src', 'tokens', 'figma-style-map.json')
const outPath = path.join(__dirname, '..', 'src', 'tokens', 'figma-tokens.css')

if (!fs.existsSync(mapPath)) {
  console.error('Missing', mapPath)
  process.exit(1)
}

const map = JSON.parse(fs.readFileSync(mapPath, 'utf8'))

function sanitize(name) {
  return name
    .replace(/^[\.\s]+/, '')
    .toLowerCase()
    .replace(/\//g, '-')
    .replace(/[^a-z0-9-]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

const lines = []
lines.push('/* Figma style tokens — generated by scripts/write-figma-tokens-css.js */')
lines.push(':root {')
for (const [styleName, info] of Object.entries(map)) {
  const hex = info.hex || null
  if (!hex) continue
  const comment = `  /* ${styleName} (id: ${info.id}) mappedTo: ${info.mappedTo || 'unknown'} */`
  const varName = `--figma-${sanitize(styleName)}`
  const line = `  ${varName}: ${hex};`
  lines.push(comment)
  lines.push(line)
}
lines.push('}')

fs.writeFileSync(outPath, lines.join('\n') + '\n')
console.log('Wrote', outPath)
